    <h3 class="mt-5 mb-5" id="h-nome-do-dom-nio">Nome do domínio</h3>
    <p>O nome do domínio é a identidade de uma empresa na internet, é a parte do domínio que você tem mais liberdade de criação. Por isso, antes de decidir, é importante escolher um nome que tenha uma forte ligação com você, seu produto, sua empresa ou o que você oferece no site.</p>
    <p>Além de representar o que você quer comunicar, é importante que o nome seja <strong>curto, amigável e fácil de lembrar</strong>.</p>
    <p>Mesmo que essa parte tenha mais liberdade, algumas <strong>regras sintáticas </strong>devem ser respeitadas:</p>
    <ul><li>Devem ter entre 2 e 26 caracteres, sem incluir a terminação;</li><li>Domínios nacionais (.br) permitem o uso de caracteres acentuados, mas eles não são recomendados porque nem todos os servidores de e-mail suportam esta opção;</li><li>Não há diferenciação entre letras maiúsculas ou minúsculas. 
            Anywhere-tecnologias.com.br será reconhecido da mesma fora que anywhere-tecnologias.com.br, por exemplo;</li><li>O nome do domínio não deve ser iniciado nem terminado por hífen.</li></ul>
