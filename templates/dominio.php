    <h3 class="mt-5 mb-5" id="h-extens-o-de-dom-nio">Extensão de domínio</h3>
    <p>A extensão de domínio, também conhecida como <strong>gTLD (Generic Top-Level Domain)</strong> ou Domínio de Primeiro Nível (DPN) em português, é a parte que determina a categoria a qual ele pertence. São formadas por mais de duas letras e podem ser usadas em qualquer localidade.</p>
    <p>A gTLD indica se é um endereço eletrônico voltado para a área comercial, educação, governo, entre outras opções. Elas ajudam na representação de uma marca ou empresa.</p>
    <p>Existem mais de 250 domínios de primeiro nível e a extensão mais conhecida (.com) já está perto de completar seus 30 anos.</p>
    <p>Atualmente diversas gTLDs novas estão sendo criadas, dessa forma elas estão sendo divididas entre novas e originais:</p>
    <ul><li><strong>gTLDs originais:</strong> .com, .org, .net, .gov, .info e outras.</li><li><strong>Novas gTLDs:</strong> .tech, .life, .top, .store, .space, .fun e outras.</li></ul>
    <p>As novas gTLDs foram criadas para ampliar as opções de nomenclatura na web. Muitas delas são para determinados <strong>setores e interesses</strong>, assim seu domínio pode expressar melhor o que você faz.</p>
    <h3 class="mt-5 mb-5" id="h-c-digo-do-pa-s">Código do país</h3>
    <p>A <strong>ccTLD (Country Code Top-Level Domain)</strong> é uma sigla de duas letras no final do domínio que indica o país de origem. Elas são consideradas extensões nacionais e normalmente estão restritas para uso de <strong>cidadãos ou empresas que atuam no determinado país</strong>, mas o código do país não é obrigatório no registro do domínio.</p>
    <p>
        No Brasil, todos os domínios com terminação .br são gerenciados pela entidade Registro.br, 
        que divulga a relação completa das 
        <a href="https://registro.br/dominio/categorias/" target="_blank" rel="noopener noreferrer">
            categorias de domínio
        </a> 
        disponíveis no seu site.
    </p>
    <p>Mesmo que as ccTLDs tenham sido criadas para uso nacional, algumas também se aplicam para outros fins, como por exemplo:</p>
    <ul><li><strong>.bz:</strong> originalmente representa o código de Belize, mas também pode ser interpretada como “business” e usada por empresas de negócios.</li><li><strong>.cc:</strong> representa o código das Ilhas Cocos, mas também é conhecida como “Creative Commons” e é usada para uma grande variedade de sites.</li><li><strong>.co:</strong> é o código da Colômbia, mas sua sigla pode significar “companhia” e acaba sendo usada para empresas, corporações e companhias de todos os segmentos.</li><li><strong>.me:</strong> representa o código de Montenegro, mas é considerada também como um tratamento pessoal e usada para blogs ou sites autorais.</li><li><strong>.tv:</strong> é, definitivamente, a extensão mais usada por seu segundo significado. Originalmente é o código de Tuvalu, mas por TV ser uma sigla popular para televisão, a extensão passou a ser usada para conteúdo de mídia e audiovisual.</li></ul>
    <p>
        Aqui no Brasil em 2017, o Registro.br iniciou uma campanha para criar novas extensões para as cidades brasileiras. Hoje, mais de 50 cidades já possuem sua própria extensão .br, veja a 
        <a rel="noopener noreferrer" href="https://cidades.registro.nic.br/" target="_blank">
            lista completa
        </a>.
    </p>
    <p>
        <strong>Dica:</strong> sempre é uma boa opção registrar o domínio escolhido com diversas terminações, 
        como: .com.br, .com, .net. Assim você garante exclusividade sobre o nome escolhido, 
        evita que seus concorrentes registrem nomes parecidos, além de ser facilmente encontrado pelo público.
    </p>

