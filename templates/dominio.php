    <h2 class="mt-5 mb-5" id="h-o-que-um-dom-nio">O que é um domínio?</h2>
    <p>
        Importante para a construção da identidade e da presença na web, 
        o domínio funciona como uma porta de acesso das pessoas ao seu site. 
        Por isso, criar um domínio que faça referência à sua empresa, ao seu produto ou ao trabalho 
        que você desenvolve é fundamental.
    </p>
    <p>
        Quando falamos em criar uma página na internet, não se trata apenas de escolher 
        um bom design ou template para usar. Você deve planejar o site que deseja construir e 
        o ponto de partida é a escolha de um nome.
    </p>
    <p>
        Podemos definir domínio como sendo o 
        <strong>nome de um site</strong>. Também conhecido como endereço eletrônico ou URL, 
        o domínio é o endereço do seu site na internet.
    </p>
    <p>É o que as pessoas vão digitar nos seus navegadores depois do www para encontrar o seu site.</p>
    <p>
        O domínio apenas carrega essa informação, a parte responsável por armazenar os dados, 
        arquivos, imagens e textos do seu site é a hospedagem de sites. 
        Juntos eles são peças essenciais para que um site fique online na internet.
    </p>
    <h3 class="mt-5 mb-5" id="h-por-que-os-dom-nios-foram-criados">Por que os domínios foram criados?</h3>
    <p>
        Todos os sites têm um endereço de IP único, que consiste em números e se conecta com o servidor que armazena os dados do site. 
        Imagine agora fazer com que o seu visitante aprenda e <strong>memorize o número de IP</strong>. 
        Seria bem difícil, não é mesmo?
    </p>
    <p>
        Mesmo que a gente tenha que memorizar documentos de identidade ou até mesmo números de telefone, 
        seria impossível decorar os endereços de IP de todos os sites que nós acompanhamos.
    </p>
    <p>Assim, um dos principais objetivos dos domínios é de <strong>facilitar a memorização</strong> do endereço de um site.</p>
    <h3 class="mt-5 mb-5" id="h-como-os-dom-nios-s-o-gerenciados">Como os domínios são gerenciados?</h3>
    <p>
        Da mesma forma que existe um IP único, os domínios também são únicos. Não existe dois domínios escritos 
        completamente iguais registrados e ativos. Por isso existe uma entidade responsável pela alocação do espaço 
        de endereços do Protocolo da Internet (IP), a 
        <a href="https://www.icann.org/" target="_blank" rel="noopener noreferrer">
            ICANN
        </a>.
    </p>
    <p>
        A ICANN é responsável por coordenar os elementos técnicos para garantir que todos os usuários consigam 
        encontrar todos os endereços válidos na internet. Para isso, ela supervisiona a distribuição dos identificadores 
        técnicos e de nomes de domínio internacionais.
    </p>
    <p>
        Aqui no Brasil para os domínios nacionais, com a terminação .br, essa administração é feita pelo 
        <a rel="noopener noreferrer" href="https://registro.br/" target="_blank">
            Registro.br
        </a>.
    </p>
    <h2 class="mt-5 mb-5" id="h-como-funciona-um-dom-nio-na-pr-tica">
        Como funciona um domínio na prática?
    </h2>
    <p>
        Ao acessar um domínio no navegador, os servidores DNS (Domain Name System, ou sistema de nomes de domínios) 
        <strong>localizam e traduzem para números IP os endereços dos sites</strong>.
    </p>
    <p>Assim, conseguimos acessar o conteúdo do domínio que foi armazenado na hospedagem através daquele determinado IP.</p>
