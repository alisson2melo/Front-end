    <h3>Armazenamento:</h3>
    <p>
        O armazenamento (também conhecido como espaço em disco) é o 
        <strong>limite de espaço que o conteúdo de um site pode utilizar</strong> 
        dentro do servidor. Então, antes de contratar um plano, é importante saber qual é o 
        tamanho que os arquivos do seu site possuem para comparar com o espaço em disco 
        disponibilizado no servidor.</p>
    <p>
        Além dos arquivos do site, os e-mails também ocupam esse espaço em disco. A maioria 
        dos sites utilizam pouco espaço e, aqui na Anywhere Tecnologias, fazemos um cálculo que já prevê 
        uma “sobra” para que futuramente o site cresça.
    </p>
    <h3>Transferência:</h3>
    <p>
        É a <strong>quantidade de dados que podem ser transferidos para a hospedagem</strong> 
        em um mês, por exemplo: quando você precisa atualizar o site, fazer upload de imagens ou 
        qualquer outro conteúdo, o recurso de transferência está sendo utilizado.
    </p>
    <p>
        Além disso, ao acessar um site é feito o download dos arquivos pelo navegador, 
        dessa forma a transferência (ou tráfego) também está relacionada à quantidade de 
        visitas que um site recebe. Porém isso não está relacionado a picos de tráfego, 
        mas sim, um tráfego constante.
    </p>
    <p>
        Outro fator que está ligado ao consumo deste recurso é a otimização do site, sendo assim, 
        os sites com melhores otimizações consomem menos recursos de transferência.
