    <h2>O que é uma Hospedagem de Site?</h2>
    <p>
        <strong>Um site é um conjunto de arquivos</strong> 
        (textos, imagens e códigos) que juntos formam as páginas que estamos acostumados 
        a encontrar na internet. Os arquivos do site são lidos por programas especializados – os navegadores, 
        mas antes esses arquivos <strong>precisam estar guardados em determinado local</strong>, 
        o que chamamos de servidor.
    </p>
    <p>
        Ter esse local para armazenar os arquivos é fundamental, pois o domínio não guarda nenhum conteúdo do site, 
        ele é apenas o endereço eletrônico (ou URL). O que faz esse papel é o servidor.
    </p>
    <p>
        <strong>Um servidor é um computador robusto</strong> 
        que possui mais recursos e melhor desempenho que um computador normal, ele possui a função de 
        <strong>
            armazenar diversos arquivos e deixá-los disponíveis para que outros computadores consigam acessar.
        </strong> Por isso dizemos o servidor “hospeda” o site, 
        foi aí que surgiu o termo <em>hospedagem de site</em>.
    </p>
    <p>
        <strong>De uma forma simples:</strong> a hospedagem é um espaço no servidor que armazena todos os arquivos 
        que compõem um site para deixá-lo acessível na internet.
    </p>

